    private void menuRelatorioClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuRelatorioClienteActionPerformed
        // gerando um relatório de clientes:
        int Confirma = JOptionPane.showConfirmDialog(null, "Confirma a impressão desse relatório?", "Atenção", JOptionPane.YES_NO_OPTION);

        if (Confirma == JOptionPane.YES_OPTION) {
            //imprimindo o relatório com o frameworc jasper
            try {
                //usando a classe jasper print
                JasperPrint imprime = JasperFillManager.fillReport("/home/wesley/Área de Trabalho/reports/clientes.jasper", null, conexao);

                //a linha abaixo exibe o relatorio atravéz da classe jasper viewer
                JasperViewer.viewReport(imprime, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_menuRelatorioClienteActionPerformed

