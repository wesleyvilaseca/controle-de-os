    //o codigo abaixo evita que janelas iguais sejam abertas no desktop
    public void comandoInternal(JInternalFrame frame) {
        for (JInternalFrame iternal : Desktop.getAllFrames()) {
            if (iternal.getClass().toString().equalsIgnoreCase(frame.getClass().toString())) {
                return;
            }
        }
        Desktop.add(frame);
        frame.setVisible(true);
    }

